    public DatosServicios(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        this.parent = parent;
        
        modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        cargarModeloTabla();
        List<ServicioDTO> servicios = servSvc.consultarServicios();
        
        // Limpiar la tabla antes de agregar nuevos datos
        modeloTabla.setRowCount(0);
        for (ServicioDTO servicio : servicios) {
            Object[] row = {
                servicio.getId(),
                servicio.getCedula(),
                servicio.getPlaca(),
                servicio.getFecha_ingreso(),
                servicio.getFecha_entrega(),
                servicio.getMotivo_ingreso(),
                servicio.getCosto(),
                servicio.getHoras_trabajo(),
                servicio.getMotivo_costo_servicio()
            };
            modeloTabla.addRow(row);
        }
        
        initComponents();
        setLocationRelativeTo(null);
    }
    
    private void cargarModeloTabla(){
        modeloTabla.addColumn("id");
        modeloTabla.addColumn("Propietario");
        modeloTabla.addColumn("Placa Vehiculo");
        modeloTabla.addColumn("Fecha ingreso");
        modeloTabla.addColumn("Fecha entrega");
        modeloTabla.addColumn("Motivo");
        modeloTabla.addColumn("Costo");
        modeloTabla.addColumn("Horas trabajo");
        modeloTabla.addColumn("Tipo costo");
    }
    
