    private void tableservsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableservsMouseClicked
        if (evt.getClickCount() == 2 && tableservs.getSelectedRow() != -1) {
            
            Object idServicio = tableservs.getValueAt(tableservs.getSelectedRow(), 0);
            List<ServicioDTO> servicio = servSvc.consultarServicios((String)idServicio);
            
            if(ObjectsUtil.vacio(servicio)){
                JOptionPane.showMessageDialog(null,"No se ha encontrado el servicio seleccionado.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            DatosNuevoServicio ventanaNS = new DatosNuevoServicio(parent,true, servicio.get(0));
            ventanaNS.setVisible(true);
        }
    }//GEN-LAST:event_tableservsMouseClicked

